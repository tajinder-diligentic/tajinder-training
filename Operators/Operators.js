//Operators are the symbols that we use in expressions to do certain things.

//Math Operators

let a = 10;
let b = 5;
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a ** b); // addds power

// Increment/Decrement Operators

let c = 5;

c = c++;
console.log(c);
c = ++c;
console.log(c);

//Assignment Operators

let d = 5;

d += 2;
console.log(d);
d -= 3;
console.log(d);

//Comparison Operators

let i = 5;
let k = 5;

console.log(i == k);
console.log(i === k);
console.log(i != k);
console.log(i < k);
console.log(i > k);
console.log(i <= k);
console.log(i >= k);

// Bitwise Operators

let g = 5;
let h = 7;

console.log(g & h);
console.log(g | h);
console.log(g ^ h);
console.log(~g);
