// The String object is used to represent and manipulate a sequence of characters.
let str = 'Iamprogramer';

let length = str.length;
console.log(length);

let individual = str[0];
console.log(individual);

let char = str.charAt(5);
console.log(char);

let index = str.indexOf('p');
console.log(index);

let trim = str.trim();
console.log(trim);

let replace = str.replace('p', 'h');
console.log(replace);

let includes = str.includes('g');
console.log(includes);

let split = str.split('');
console.log(split);
